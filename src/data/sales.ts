function getSaleCopy(title: string): SaleCopy {
  if (title.includes("楽天スーパーSALE") || title.includes("楽天お買い物マラソン")) return saleCopy.rakutenShoppingCampaign;
  if (title.includes("楽天ブラックフライデー")) return saleCopy.rakutenBlackFriday;
  if (title.includes("楽天大感謝祭")) return saleCopy.rakutenYearEndThanks;
  if (title.includes("初売り／新春ポイントアップ祭") || title.includes("初売り福袋特集")) return saleCopy.rakutenNewYear;
  if (title.includes("Amazon プライムデー") || title.includes("Amazon プライム感謝祭")) return saleCopy.amazonPrime;
  if (title.includes("Amazon ブラックフライデー")) return saleCopy.amazonBlackFriday;
  if (title.includes("Amazon 新生活") || title.includes("Amazon スマイルSALE")) return saleCopy.amazonSeasonal;
  if (title.includes("Amazon 初売り")) return saleCopy.amazonNewYear;
  if (title.includes("超PayPay祭")) return saleCopy.yahooSuperPayPay;
  if (title.includes("ヤフービッグボーナス")) return saleCopy.yahooBigBonus;
  if (title.includes("爆買ウルトラSALE")) return saleCopy.yahooUltraSale;
  throw new Error(`セール説明文が未定義です: ${title}`);
}

